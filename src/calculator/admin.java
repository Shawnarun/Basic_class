 public void loadData() {
        // Object columns[] = {"Name"," ID", "Payment"};
        String sql = "SELECT `studentname`, `studentid`, `payment`, `id` FROM `student`";
        try {
            pre = con.prepareStatement(sql);
            rs = pre.executeQuery();
            Object columnData[] = new Object[3];
            while (rs.next()) {
                columnData[0] = rs.getString("studentname");
                columnData[1] = rs.getString("studentid");
                columnData[2] = rs.getString("payment");
                
                  defaultTableModel.addRow(columnData);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
        sn.setText(null);
        sid.setText(null);  
        pnt.setText(null);
    }
