    private void addMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_addMouseClicked
        // TODO add your handling code here:
         String cname=sn.getText();
         String si=sid.getText();
        Double amount=Double.parseDouble(pnt.getText());
        
         if(cname.equals("")||si.equals("")){

            JOptionPane.showMessageDialog(null,"Complete Your Account Information","Missing Information",2);

        }
        else {
            String sql = "INSERT INTO `student`(`studentname`, `studentid`, `payment`)  VALUES (?,?,?)";
            if (con != null) {

                try {
                    pre = con.prepareStatement(sql);
                    pre.setString(1, cname);
                    pre.setString(2,si );
                     pre.setDouble(3, amount);
                    
                    
                    pre.execute();
                    defaultTableModel.getDataVector().removeAllElements();
                    defaultTableModel.fireTableDataChanged();
                    loadData();
                    JOptionPane.showMessageDialog(null, "Data Saved");
                } catch (SQLException e) {
                    JOptionPane.showMessageDialog(null, e);
                }
            }
        }
        
    }//GEN-LAST:event_addMouseClicked

